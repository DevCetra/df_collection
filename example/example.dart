  // Traverse a map using a list of keys and a set a new value.
  Map buffer = {};
  buffer.traverse([1, 2, 3, 4], newValue: 5);
  print(buffer); // {1: {2: {3: {4: 5}}}}
  print(buffer.traverse([1, 2, 3, 4])); // 5
}
