extension TraverseMapOnMapExtension<K, V> on Map<K, V> {
  /// See [traverseMap].
  dynamic traverse(
    List<dynamic> keys, {
    dynamic newValue,
  }) {
    return traverseMap(this, keys, newValue: newValue);
  }
}
