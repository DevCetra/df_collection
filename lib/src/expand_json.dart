import 'expand_flattened_json.dart';
import 'flatten_json.dart';

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

/// Expands a Json map, e.g. `{'a': {'b': 1}}` to `{a.b: 1, b: 1}`.
Map<String, dynamic> expandJson(
  Map<String, dynamic> input, {
  String separator = '.',
}) {
  return expandFlattenedJson(
    flattenJson(
      input,
      separator: separator,
    ),
    separator: separator,
  );
}